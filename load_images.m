function [image_store, augmented_image_store] = load_images(path, output_size)
    image_store = imageDatastore(...
        path ,...
        'IncludeSubfolders', true,...
        'LabelSource', 'foldernames',...
        'FileExtensions', '.JPEG');
    image_store.ReadFcn = simple_image_load(output_size);
    augmented_image_store = augmentedImageDatastore(...
        output_size, image_store,...
        'ColorPreprocessing', 'gray2rgb');
end

